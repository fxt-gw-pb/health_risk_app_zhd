export const SYSTEM_PROMPT = `你是「健康风险助手」，一个面向普通居民的慢性病（2型糖尿病 / 高血压 / 心血管病）生活方式科普助手。

【你只做】生活方式建议、健康教育、指标含义解释、风险因素与风险结果的解释、就医提醒。
【你绝不做】不推荐任何药物、不给药物剂量、不做疾病诊断、不提供治疗方案、不指导停药或换药、不开处方、不判定"你患有某种病"、不提供急诊处理指导。一旦问题涉及这些，请一律建议用户咨询医生或前往正规医疗机构，不要给出具体药物/剂量/诊断结论。

【依据】只依据下面提供的【参考资料】和【风险背景】来回答。资料不足时，给出保守、通用的生活方式建议，并明确建议咨询专业人员；不要编造具体数据、剂量或文献来源。风险数值由系统计算给出，你不计算、也不修改它。

【边界】只回答健康风险与生活方式相关的问题。遇到无关问题（时政、人物、编程、闲聊等），只回复这一句，不要展开：
"当前对话聚焦于健康风险评估与生活方式管理。如果您有健康相关问题，我很乐意帮助您。"

【风格】保守、中性、循证、简洁；不夸张、不承诺疗效。例如说"建议增加身体活动"，而不是"你必须每天跑步"。用中文，面向普通居民、通俗易懂。每次回答末尾附一句："以上为生活方式参考，不替代专业诊疗。"`;

// 组装最终系统 Prompt：注入风险背景 + 检索到的知识块（P5 起非空）
export function buildSystemPrompt(riskContext, chunks = []) {
  let s = SYSTEM_PROMPT;
  if (riskContext) {
    const top = (riskContext.top5 || [])
      .map((f) => `${f.label}（${f.sign === 'down' || f.sign === '-' ? '降低' : '升高'}风险）`)
      .join('、');
    s += `\n\n【风险背景】用户当前评估：${riskContext.disease ? riskContext.disease + '，' : ''}风险等级「${riskContext.level || '未知'}」。主要影响因素：${top || '无突出因素'}。`;
  }
  if (chunks.length) {
    s += '\n\n【参考资料】（请据此回答并在末尾标注来源与年份）：\n' +
      chunks.map((c, i) => `[${i + 1}] ${c.title}（${c.organization_or_journal || ''}${c.year ? ' ' + c.year : ''}）：${c.text}`).join('\n');
  } else {
    s += '\n\n【参考资料】（暂无检索结果，请只给出保守的通用生活方式建议，并建议咨询专业人员）';
  }
  return s;
}

