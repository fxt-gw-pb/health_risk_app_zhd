// 出口安全过滤：药物 / 剂量 / 诊断断言 命中即替换
const DRUG_TERMS = [
  '二甲双胍', '格列', '磺脲', '磺酰脲', 'SGLT-2', 'SGLT2', 'GLP-1', '胰岛素',
  '他汀', '阿托伐他汀', '瑞舒伐他汀', '阿司匹林', '氯吡格雷', '普利', '沙坦',
  '氨氯地平', '硝苯地平', '缬沙坦', '美托洛尔', '氢氯噻嗪', '利尿剂',
];
const DOSE_PAT = /\d+\s*(mg|毫克|g|克|IU|单位)\s*(每日|每天|qd|bid|tid|一次|次)|每(日|天)\s*\d+\s*(次|片|粒|mg|毫克)/i;
const DIAG_PAT = /(确诊|诊断)(为|是)|你(已经)?(患有|得了|患上)|可以确定(你|您)(患|有)/;

export function scanForbidden(text) {
  if (!text) return false;
  if (DOSE_PAT.test(text) || DIAG_PAT.test(text)) return true;
  return DRUG_TERMS.some((d) => text.includes(d));
}

